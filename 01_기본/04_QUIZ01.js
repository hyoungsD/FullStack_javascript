// 학점 구하기
let score = 89;
let grade;

if(score >= 90){
    grade = 'A';
}else if(score >= 80){
    grade = 'B';
}else if(score >= 70){
    grade = 'C';
}else if(score >= 60){
    grade = 'D';
}else{
    grade = 'F';
}
console.log(`당신의 등급은 ${grade}입니다`);


