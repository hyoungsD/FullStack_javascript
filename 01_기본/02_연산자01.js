// 연산자


// 산술연산자
let num1 = 5;
let num2 = 3;

console.log('-num1', -num1);
console.log('num1 + num2', num1 + num2);
console.log('num1 - num2', num1 - num2);
console.log('num1 * num2', num1 * num2);
console.log('num1 / num2', num1 / num2);
console.log('num1 % num2', num1 % num2);


// 비교연산자
console.log('123 == 123', 123 == 123);
console.log('123 == "123"', 123 == "123");
console.log('123 === "123"', 123 === "123");
console.log('123 === 123', 123 === 123);

console.log('123 != 123', 123 != 123);
console.log('123 != "123"', 123 != "123");
console.log('123 !== "123"', 123 !== "123");
console.log('123 !== 123', 123 !== 123);


